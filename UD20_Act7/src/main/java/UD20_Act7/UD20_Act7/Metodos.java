package UD20_Act7.UD20_Act7;

public class Metodos {

	public static String calcularEurPts(int entrada) {
		
		double resn = entrada * 166.386;
		
		String res = resn + "";
		
		return res;
		
	}
	
	public static String calcularPtsEur(int entrada) {
		
		double resn = entrada / 166.386;
		
		String res = resn + "";
		
		return res;
		
	}
	
}
